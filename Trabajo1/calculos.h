#ifndef CALCULOS_H_INCLUDED
#define CALCULOS_H_INCLUDED

#endif // CALCULOS_H_INCLUDED

/** \brief Realiza la operacion de suma
 *
 * \param x int Operando A ingresado
 * \param y int Operando B ingresado
 * \return int Resultado de la suma
 *
 */
int sumando(int x, int y);

/** \brief Realiza la operacion de resta
 *
 * \param x int Operando A ingreasado
 * \param y int Operando B ingresado
 * \return int Resultado de la resta
 *
 */
int restando(int x, int y);

/** \brief Realiza la operacion de dividir
 *
 * \param x int Operando A ingresado
 * \param y int Operando B ingresado
 * \return float Resultado de la division en flotante
 *
 */
float dividiendo(int x, int y);

/** \brief Realiza la operacion de multiplicar
 *
 * \param x int Operando A ingresado
 * \param y int Operando B ingresado
 * \return int Resultado de la multiplicacion
 *
 */
int multiplicar(int x, int y);

/** \brief Realiza el factorial
 *
 * \param x int Operando A o B ingresado
 * \return int Resultado del factorial
 *
 */
int factorizar(int x);
